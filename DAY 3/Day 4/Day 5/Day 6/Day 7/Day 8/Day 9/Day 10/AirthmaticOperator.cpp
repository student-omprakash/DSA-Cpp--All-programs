# include<iostream>
using namespace std;
int main(){
    cout<<5/2<<" ";
    cout<<5.2/2<<" ";
    cout<<5*3<<" ";
    cout<<5.2*3;
    int a=10;
    cout<<a++<<" ";//here first 'a' will print 10 after 'a' will become 11
    cout<<++a<<" ";//here a=11 after that a will increase by 1 and will print 12
    int b=5;
    cout<<b--<<" ";
    cout<<--b;
    return 0;
}