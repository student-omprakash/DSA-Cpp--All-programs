//comparision operators
# include<iostream>
using namespace std;
int main(){
    int a,b;
    cout<<"Enter The Values:";
    cin>>a>>b;
    //== operator
    if(a==b) cout<<"Yes";
    else cout<<"No";
    cout<<endl;
    //>= operator
    if(a>=b) cout<<"Yes";
    else cout<<"No";
    cout<<endl;
    //<=operator
    if(a<=b) cout<<"Yes";
    else cout<<"No";
    cout<<endl;
    //> operator
    if(a>b) cout<<"yes";
    else cout<<"No";
    cout<<endl;
    //< operator
    if(a<b) cout<<"Yes";
    else cout<<"No";
    cout<<endl;
    if(a!=b) cout<<"Yes";
    else cout<<"No";
    cout<<endl;


    return 0;
}